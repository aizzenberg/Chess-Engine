def _flip_pst_table(pst_table: list[int]):
    """
    Vertically mirrors the pst tables, so that python-chess square indexing corresponds our visual representation
    """
    for sqr_idx in range(len(pst_table) // 2):
        row = sqr_idx // 8
        col = sqr_idx % 8
        mir_idx = (7 - row) * 8 + col
        mir = pst_table[mir_idx]
        pst_table[mir_idx] = pst_table[sqr_idx]
        pst_table[sqr_idx] = mir
    return pst_table


